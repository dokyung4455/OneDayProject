package com.callor.score.model;

public class ScoreVO {
	private String names;
	private Integer kor;
	private Integer eng;
	private Integer math;
	private Integer scien;
	private Integer khis;
	private Integer sum;
	private Float avg;

	public String getNames() {
		return names;
	}

	public void setNames(String names) {
		this.names = names;
	}

	public Integer getKor() {
		return kor;
	}

	public void setKor(Integer kor) {
		this.kor = kor;
	}

	public Integer getEng() {
		return eng;
	}

	public void setEng(Integer eng) {
		this.eng = eng;
	}

	public Integer getMath() {
		return math;
	}

	public void setMath(Integer math) {
		this.math = math;
	}

	public Integer getScien() {
		return scien;
	}

	public void setScien(Integer scien) {
		this.scien = scien;
	}

	public Integer getKhis() {
		return khis;
	}

	public void setKhis(Integer khis) {
		this.khis = khis;
	}

	public Integer getSum() {
		return sum;
	}

	public void setSum(Integer sum) {
		this.sum = sum;
	}

	public Float getAvg() {
		return avg;
	}

	public void setAvg(Float avg) {
		this.avg = avg;
	}


	
	

}
